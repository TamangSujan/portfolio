function createKnowledge(page){
    if(page < 1)
        page = 1;
    removeKnowledge();
    let start = (page - 1) * pageSize;
    let end = start + pageSize;
    if(end > knowledge.length)
        end = knowledge.length;
    let parent = document.getElementById("major_knowledge");
    for(let index=start; index<end; index++){
        let certification = createKnowledgeModel(knowledge[index]);
        certification.setAttribute('class', 'major_knowledge_model');
        parent.appendChild(certification);
    }
}

function createKnowledgeModel(model){
    let knowledge = document.createElement('div');
    let image = document.createElement('div');
    let info = document.createElement('div');

    let logo = document.createElement('img')
    let title= document.createElement('label');
    let associationParent= document.createElement('div');
    let association= document.createElement('label');
    let associationUrl= document.createElement('a');
    let inAssociationWith= document.createElement('label');
    let knowledgeUrl= document.createElement('a');
    let description = document.createElement('label');

    logo.setAttribute('src', model.logo);
    title.innerHTML = model.title;
    association.innerHTML = model.association;
    associationUrl.setAttribute('href', model.association_url);
    associationUrl.setAttribute('target', '_blank');
    inAssociationWith.innerHTML = 'In Association with ';
    knowledgeUrl.setAttribute('href', model.knowledge_url);
    knowledgeUrl.setAttribute('target', '_blank');
    description.innerHTML = model.description;

    title.setAttribute('class', 'major_knowledge_title');
    associationParent.setAttribute('class', 'major_knowledge_association');
    description.setAttribute('class', 'major_knowledge_description');

    knowledgeUrl.appendChild(logo);
    associationUrl.appendChild(association);
    associationParent.appendChild(inAssociationWith);
    associationParent.appendChild(associationUrl);

    image.setAttribute('class', 'major_knowledge_model_image');
    info.setAttribute('class', 'major_knowledge_model_info');

    image.appendChild(knowledgeUrl);
    info.appendChild(title);
    info.appendChild(associationParent);
    info.appendChild(description);

    knowledge.appendChild(image);
    knowledge.appendChild(info);
    return knowledge;
}

function removeKnowledge(){
    let parent = document.getElementById("major_knowledge");
    let children = parent.children;
    for(let index=0; index<children.length; index++){
        parent.removeChild(children[index]);
    }
}

createKnowledge(1);